int main(int argc, char *argv[])
{
    srand(time(NULL)); /* This will ensure a random game each time. */

    int random = rand()%4+1;
    printf("%d\n", random);
    double time = 2200;

    while(time>0){
        // time-= controllerTime;
        int input;
        scanf("%d", &input); 
        if (input == random){
            printf("Correct\n");
        }
        time-=1;
        printf("%.2lf\n", time);
    }

    return 0;


}

